// Declarar dos variables numéricas e incrementar una de ellas y decrementar la otra en una sola línea de código 
// utilizando operadores de incremento y decremento. Muestra los valores resultantes.

var numero1 = 3;
var numero2 = 9;

numero1++;
numero2--;

console.log("El primer número luego de incrementarlo: " + numero1);
console.log("El segundo número luego del decremento: " + numero2);